package step.plugins.screentemplating;

import step.commons.activation.AbstractActivableObject;
import step.commons.activation.Expression;

public class Option extends AbstractActivableObject {
	
	String value;

	public Option() {
		super();
	}

	public Option(String value) {
		super();
		this.value = value;
	}

	public Option(String value, String activationExpression) {
		super();
		this.setActivationExpression(new Expression(activationExpression));
		this.value = value;
	}
	
	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((value == null) ? 0 : value.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Option other = (Option) obj;
		if (value == null) {
			if (other.value != null)
				return false;
		} else if (!value.equals(other.value))
			return false;
		return true;
	}
}
