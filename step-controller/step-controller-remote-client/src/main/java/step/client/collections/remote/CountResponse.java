package step.client.collections.remote;

public class CountResponse {

	private Long count;

	public CountResponse() {
		super();
	}

	public CountResponse(Long count) {
		super();
		this.count = count;
	}

	public Long getCount() {
		return count;
	}

	public void setCount(Long count) {
		this.count = count;
	}

}
